package com.manager.farm;

public class Transfer {
    private int id;
    private String achizitie;
    private String vanzzare;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAchizitie() {
        return achizitie;
    }

    public void setAchizitie(String achizitie) {
        this.achizitie = achizitie;
    }

    public String getVanzzare() {
        return vanzzare;
    }

    public void setVanzzare(String vanzzare) {
        this.vanzzare = vanzzare;
    }
}
